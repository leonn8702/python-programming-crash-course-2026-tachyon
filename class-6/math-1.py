import math
number = 64
square_root = math.sqrt(number) # sqrt() ফাংশন বর্গমূল নির্ণয় করে
print(f"{number}-এর বর্গমূল হলো {square_root}") # আউটপুট: 8.0
print(f"পাই (π) এর মান: {math.pi}") # math.pi একটি কনস্ট্যান্ট
print(math.ceil(4.2))  # আউটপুট: 5 (পরবর্তী পূর্ণসংখ্যা)
print(math.floor(4.9)) # আউটপুট: 4 (পূর্ববর্তী পূর্ণসংখ্যা)
